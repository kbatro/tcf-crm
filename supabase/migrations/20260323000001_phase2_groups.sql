-- Phase 2: Companies → Groups migration
-- Creates group_types, groups, group_properties, group_members tables
-- Migrates existing companies data into the new structure

-- 1. Create new tables
create table public.group_types (
    id bigint generated by default as identity primary key,
    name text not null,
    schema jsonb not null default '{}'::jsonb
);

create table public.groups (
    id bigint generated by default as identity primary key,
    name text not null,
    group_type_id bigint references public.group_types(id),
    avatar jsonb,
    actor_id bigint references public.actors(id),
    created_at timestamp with time zone not null default now()
);

create table public.group_properties (
    id bigint generated by default as identity primary key,
    group_id bigint not null references public.groups(id) on update cascade on delete cascade,
    key text not null,
    value text,
    type text not null default 'text'
);

create table public.group_members (
    id bigint generated by default as identity primary key,
    group_id bigint not null references public.groups(id) on update cascade on delete cascade,
    contact_id bigint not null references public.contacts(id) on update cascade on delete cascade,
    role text,
    joined_at timestamp with time zone not null default now(),
    left_at timestamp with time zone
);

-- Indexes
create index group_properties_group_id_idx on public.group_properties using btree (group_id);
create index group_members_group_id_idx on public.group_members using btree (group_id);
create index group_members_contact_id_idx on public.group_members using btree (contact_id);

