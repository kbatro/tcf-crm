create or replace view public.activity_log with (security_invoker = on) as
select
    ('group.' || g.id || '.created') as id,
    'company.created' as type,
    g.created_at as date,
    g.id as group_id,
    g.actor_id,
    to_json(g.*) as company,
    null::json as contact,
    null::json as intention,
    null::json as note
from public.groups g
union all
select
    ('contact.' || co.id || '.created') as id,
    'contact.created' as type,
    co.first_seen as date,
    (select gm.group_id from public.group_members gm where gm.contact_id = co.id limit 1) as group_id,
    co.actor_id,
    null::json as company,
    to_json(co.*) as contact,
    null::json as intention,
    null::json as note
from public.contacts co
union all
select
    ('intention.' || i.id || '.created') as id,
    'intention.created' as type,
    i.created_at as date,
    case when i.target_type = 'group' then i.target_id else null end as group_id,
    (select a.actor_id from public.assignments a where a.intention_id = i.id and a.role = 'owner' limit 1) as actor_id,
    null::json as company,
    null::json as contact,
    to_json(i.*) as intention,
    null::json as note
from public.intentions i
union all
select
    ('note.' || n.id || '.created') as id,
    'note.created' as type,
    n.created_at as date,
    case
        when n.target_type = 'contact' then (select gm.group_id from public.group_members gm where gm.contact_id = n.target_id limit 1)
        when n.target_type = 'intention' then (select case when i.target_type = 'group' then i.target_id else null end from public.intentions i where i.id = n.target_id limit 1)
        else null
    end as group_id,
    n.actor_id,
    null::json as company,
    null::json as contact,
    null::json as intention,
    to_json(n.*) as note
from public.notes n;

