-- Phase 3: Deals → Intentions + Unified Notes
-- This migration:
-- 1. Creates intentions, intention_contacts, assignments, notes tables
-- 2. Migrates data from deals → intentions, contact_notes/deal_notes → notes
-- 3. Drops deals, contact_notes, deal_notes tables
-- 4. Updates functions, triggers, views, policies, grants

-- ============================================================
-- 1. Create new tables
-- ============================================================

create table public.intentions (
    id bigint generated by default as identity primary key,
    name text not null,
    type text,
    description text,
    target_type text,
    target_id bigint,
    status text not null,
    outcome text,
    amount bigint,
    expected_closing_date date,
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null default now(),
    archived_at timestamp with time zone,
    index smallint
);

create table public.intention_contacts (
    id bigint generated by default as identity primary key,
    intention_id bigint not null,
    contact_id bigint not null
);

create table public.assignments (
    id bigint generated by default as identity primary key,
    intention_id bigint not null,
    actor_id bigint,
    role text
);

create table public.notes (
    id bigint generated by default as identity primary key,
    target_type text not null,
    target_id bigint not null,
    text text,
    actor_id bigint,
    status text,
    created_at timestamp with time zone not null default now(),
    attachments jsonb[]
);

-- Foreign keys
alter table public.intention_contacts
    add constraint intention_contacts_intention_id_fkey foreign key (intention_id) references public.intentions(id) on update cascade on delete cascade;
alter table public.intention_contacts
    add constraint intention_contacts_contact_id_fkey foreign key (contact_id) references public.contacts(id) on update cascade on delete cascade;
alter table public.assignments
    add constraint assignments_intention_id_fkey foreign key (intention_id) references public.intentions(id) on update cascade on delete cascade;
alter table public.assignments
    add constraint assignments_actor_id_fkey foreign key (actor_id) references public.actors(id);
alter table public.notes
    add constraint notes_actor_id_fkey foreign key (actor_id) references public.actors(id);

-- Indexes
create index intention_contacts_intention_id_idx on public.intention_contacts using btree (intention_id);
create index intention_contacts_contact_id_idx on public.intention_contacts using btree (contact_id);
create index assignments_intention_id_idx on public.assignments using btree (intention_id);
create index notes_target_idx on public.notes using btree (target_type, target_id);

-- ============================================================
-- 2. Migrate data
-- ============================================================

-- 2a. deals → intentions
insert into public.intentions (id, name, type, description, target_type, target_id, status, amount, expected_closing_date, created_at, updated_at, archived_at, index)
select id, name, category, description,
    case when group_id is not null then 'group' else null end,
    group_id,
    stage, amount, expected_closing_date, created_at, updated_at, archived_at, index
from public.deals;

-- Sync the sequence
select setval('public.intentions_id_seq', greatest(coalesce((select max(id) from public.intentions), 0), 1));

-- 2b. deals.actor_id → assignments (role='owner')
insert into public.assignments (intention_id, actor_id, role)
select id, actor_id, 'owner'
from public.deals
where actor_id is not null;

-- 2c. deals.contact_ids → intention_contacts
insert into public.intention_contacts (intention_id, contact_id)
select d.id, unnest(d.contact_ids)
from public.deals d
where d.contact_ids is not null and array_length(d.contact_ids, 1) > 0;

-- 2d. contact_notes → notes (target_type='contact')
insert into public.notes (target_type, target_id, text, actor_id, status, created_at, attachments)
select 'contact', contact_id, text, actor_id, status, coalesce(date, now()), attachments
from public.contact_notes;

-- 2e. deal_notes → notes (target_type='intention')
-- deal_notes.deal_id maps directly to intention.id since we preserved the same IDs
insert into public.notes (target_type, target_id, text, actor_id, status, created_at, attachments)
select 'intention', deal_id, text, actor_id, null, coalesce(date, now()), attachments
from public.deal_notes;

